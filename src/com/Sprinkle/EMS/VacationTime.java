package com.Sprinkle.EMS;


public class VacationTime {
	private int daysAccured;
	private int daysUsed;
	private int daysUntill;
	public int getDaysAccured() {
		return daysAccured;
	}
	public void setDaysAccured(int daysAccured) {
		this.daysAccured = daysAccured;
	}
	public int getDaysUsed() {
		return daysUsed;
	}
	public void setDaysUsed(int daysUsed) {
		this.daysUsed = daysUsed;
	}
	public int getDaysUntill() {
		return daysUntill;
	}
	public void setDaysUntill(int daysUntill) {
		this.daysUntill = daysUntill;
	}
	
	
}
